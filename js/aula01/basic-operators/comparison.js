// operadores de comparação

// igualdade
console.log(5 == '5')

// igualdade estrita
console.log(5 === '5')

// diferente
console.log(5 != '5')

// diferente estrita
console.log(5 !== '5')

// maior
console.log(10 > 5)

// maior ou igual
console.log(10 >= 5)

// menor
console.log(10 < 5)

// menor igual
console.log(10 <= 5)

let podeBeber = 18 >= 21
console.log(podeBeber)

